<script>
function sendVerificationOTP() {
    const emailField = document.getElementById('email-field');
    const firstNameField = document.querySelector('input[name="first_name"]');
    const lastNameField = document.querySelector('input[name="last_name"]');
    const phoneField = document.getElementById('phone-field');
    const sendBtn = document.getElementById('send-otp-btn');
    const statusMsg = document.getElementById('otp-status-msg');

    const email = emailField.value.trim();
    const firstName = firstNameField.value.trim();
    const lastName = lastNameField.value.trim();
    const phone = phoneField.value.trim();

    if (!firstName) {
        alert('Please fill in your First Name before requesting an OTP.');
        firstNameField.focus();
        return;
    }

    if (!lastName) {
        alert('Please fill in your Last Name before requesting an OTP.');
        lastNameField.focus();
        return;
    }

    const fullName = firstName + ' ' + lastName;

    if (!email) {
        alert('Please fill in your Email Address before requesting an OTP.');
        emailField.focus();
        return;
    }

    if (!phone) {
        alert('Please fill in your Contact Number before requesting an OTP.');
        phoneField.focus();
        return;
    }

    if (!/^09\d{9}$/.test(phone)) {
        alert('Contact number must be a strict PH mobile format (11 digits starting with 09).');
        phoneField.focus();
        return;
    }

    // Disable button and show loading state
    sendBtn.disabled = true;
    const originalText = sendBtn.innerHTML;
    sendBtn.innerHTML = 'Sending...';
    
    statusMsg.classList.remove('hidden', 'text-red-600', 'text-green-600');
    statusMsg.classList.add('text-primary');
    statusMsg.textContent = 'Checking details and generating OTP...';
    statusMsg.style.display = 'block';

    const fd = new FormData();
    fd.append('email', email);
    fd.append('full_name', fullName);
    fd.append('phone', phone);

    fetch('../actions/send_otp.php', {
        method: 'POST',
        body: fd
    })
    .then(res => res.json())
    .then(data => {
        sendBtn.disabled = false;
        sendBtn.innerHTML = originalText;

        if (data.success) {
            statusMsg.classList.remove('text-primary', 'text-red-600');
            statusMsg.classList.add('text-green-600');
            statusMsg.textContent = data.message;
            // Focus on OTP input
            document.querySelector('input[name="otp_code"]').focus();
        } else {
            statusMsg.classList.remove('text-primary', 'text-green-600');
            statusMsg.classList.add('text-red-600');
            statusMsg.textContent = data.message;
        }
    })
    .catch(() => {
        sendBtn.disabled = false;
        sendBtn.innerHTML = originalText;
        statusMsg.classList.remove('text-primary', 'text-green-600');
        statusMsg.classList.add('text-red-600');
        statusMsg.textContent = 'Network error. Please try again.';
    });
}

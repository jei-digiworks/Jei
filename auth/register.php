        $sess_email = $_SESSION['reg_otp_email'] ?? '';
        $sess_time = $_SESSION['reg_otp_time'] ?? 0;

        if (empty($sess_otp) || empty($sess_email)) {
            $error = 'Please request an OTP verification code first.';
        } elseif (strtolower($email) !== strtolower($sess_email)) {
            $error = 'The registered email does not match the email used to request the OTP.';
        } elseif ($otp_code !== (string)$sess_otp) {
            $error = 'Invalid OTP code. Please check your email and try again.';
        } elseif ((time() - $sess_time) > 600) {
            $error = 'The OTP code has expired (valid for 10 minutes). Please request a new one.';
        } else {
            // Check if email already exists
            $stmt = $pdo->prepare("SELECT id FROM users WHERE email = ? LIMIT 1");
            $stmt->execute([$email]);
            if ($stmt->fetch()) {
                $error = 'An account with this email address already exists.';
            } else {
                // Check if phone number already exists
                $stmt_ph = $pdo->prepare("SELECT id FROM users WHERE phone = ? LIMIT 1");
                $stmt_ph->execute([$phone]);
                if ($stmt_ph->fetch()) {
                    $error = 'An account with this phone number already exists.';
                } else {
                    // Generate secure temporary password
                    $temp_password = "RDG-" . mt_rand(100000, 999999);
                    $password_hash = password_hash($temp_password, PASSWORD_BCRYPT);
                    
                    try {
                        $stmt = $pdo->prepare("
                            INSERT INTO users (email, password_hash, full_name, phone, role, email_verified, is_temp_password) 
                            VALUES (?, ?, ?, ?, 'client', 1, 1)
                        ");
                        $stmt->execute([$email, $password_hash, $full_name, $phone]);
                        $user_id = $pdo->lastInsertId();

                        // Generate welcome notification
                        create_notification(
                            $pdo, 
                            $user_id, 
                            null, 
                            null, 
                            'booking_confirmed', 
                            "Welcome to RDG Tennis, " . htmlspecialchars($full_name) . "! Your athlete account has been successfully created. Your temporary login password is: " . $temp_password
                        );

                        // Clear OTP from session
                        unset($_SESSION['reg_otp']);
                        unset($_SESSION['reg_otp_email']);
                        unset($_SESSION['reg_otp_time']);

                        // Email the user their temporary password
                        $email_body = '
                        <h2>Welcome to RDG Tennis!</h2>
                        <p>Hi ' . htmlspecialchars($full_name) . ',</p>
                        <p>Excellent news! Your athlete profile has been successfully registered and your email verified.</p>
                        
                        <div style="background-color: #bcf0ae; border: 2px solid #154212; padding: 20px; margin: 25px 0; color: #002201; font-weight: bold; border-radius: 0px;">
                            ✓ Email Verified & Profile Created Successfully!
                        </div>

                        <p>To access the player portal, log in using the temporary secure password generated by the system:</p>

                        <table class="details-table" style="width: 100%; border-collapse: collapse; margin: 25px 0; border: 2px solid #154212;">
                            <tr>
                                <th style="background-color: #f0eded; padding: 12px; font-weight: bold; text-transform: uppercase; font-size: 11px; border: 1px solid #154212; color: #154212; text-align: left; width: 35%;">Login Email</th>
                                <td style="padding: 12px; border: 1px solid #154212; font-size: 14px; color: #42493e;">' . htmlspecialchars($email) . '</td>
                            </tr>
                            <tr>
                                <th style="background-color: #f0eded; padding: 12px; font-weight: bold; text-transform: uppercase; font-size: 11px; border: 1px solid #154212; color: #154212; text-align: left;">Temporary Password</th>
                                <td style="padding: 12px; border: 1px solid #154212; font-size: 14px; font-family: monospace; font-weight: bold; color: #154212;">' . htmlspecialchars($temp_password) . '</td>
                            </tr>
                        </table>

                        <p style="background-color: #ffdad6; border: 2px solid #ba1a1a; padding: 15px; font-size: 12px; color: #ba1a1a; font-weight: bold; margin: 25px 0;">
                            ⚠ Security Warning: For security purposes, please login using this temporary password and update your password immediately in your account settings.
                        </p>

                        <p style="text-align: center; margin-top: 30px;">
                            <a href="http://localhost/RDG/auth/login.php" style="display: inline-block; background-color: #FFE500; color: #154212; text-decoration: none; padding: 15px 30px; font-weight: 900; text-transform: uppercase; border: 2px solid #154212; box-shadow: 4px 4px 0px #154212; font-size: 13px; text-align: center;">Log In to Athlete Portal</a>
                        </p>';
                        
                        send_html_email($email, "Welcome to RDG Tennis! Your Temporary Access Credentials", $email_body);

                        // Set success flags for the UI instruction modal
                        $registration_success = true;
                        $success_email = $email;
                        $success_temp_password = $temp_password;

                    } catch (PDOException $e) {
                        $error = 'Registration failed: ' . $e->getMessage();
                    }
                }
            }
        }
    }
